    _G.SlashCmdList["CAPTAINSLOG"]("status")
    assertTrue(containsPrefix(ctx.chatLines, "|cff00ff00[Captain's Log]|r Status: mode=manual zone=Zul'Gurub logging=on"), "expected manual status output")
end

